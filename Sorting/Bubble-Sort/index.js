function bubbleSorting(arr, n) {
  for (let i = 0; i < n - 1; i++) {
    for (let j = 0; j < n - i - 1; j++) {
      if (arr[j] > arr[j + 1]) {
        let temp = arr[j];
        arr[j] = arr[j + 1];
        arr[j + 1] = temp;
      }
    }
  }
}

const nums = [5, 2, 3, 6, 4, 7];
bubbleSorting(nums, nums.length);
console.log(nums);

